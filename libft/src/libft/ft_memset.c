#include "../../include/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*aux;

	aux = (unsigned char *) b;
	i = 0;
	while (i < len)
	{
		aux[i] = c;
		i++;
	}
	return (b);
}
